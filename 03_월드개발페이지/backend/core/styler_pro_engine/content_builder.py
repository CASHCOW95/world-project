def clean_jargon(text):
    if not isinstance(text, str):
        return text
    cleaned = text
    for eng, kor in JARGON_MAP.items():
        pattern = re.compile(eng, re.IGNORECASE)
        cleaned = pattern.sub(kor, cleaned)
    return cleaned

